        public void verificaCampos() {
        if (verificaDias() && verificaFecha()&& verificaHora()) {
            btn_RealizarOperacion.setEnabled(true);
            lblmensaje.setText("");
        } else {
            if (!verificaDias()) {
                lblmensaje.setText("Debe seleccionar un Dia al menos \n");
            }
            if (!verificaHora()) {
                lblmensaje.setText("La hora de Inicio debe de ser anterior a la final \n");
            }
            if (!verificaFecha()) {
                lblmensaje.setText("Debe seleccionar una fecha correcta \n");
            }
            btn_RealizarOperacion.setEnabled(false);
        }
    }
        
    public boolean verificaDias() {
        return rbLunes.isSelected() || rbMartes.isSelected() || rbMiercoles.isSelected() || rbJueves.isSelected() || rbViernes.isSelected() || rbSabado.isSelected() || rbDomingo.isSelected();
    }
    public boolean verificaFecha(){
        if (tmp_Fecha.getDate() != null){
            return true;
        }
        return false;
    }

    public boolean verificaHora() {
        if (tmp_HoraInicio.getTime() != null && tmp_HoraFinal.getTime() != null) {
            return tmp_HoraInicio.getTime().isBefore(tmp_HoraFinal.getTime());
        } else {
            return false;
        }
    }

