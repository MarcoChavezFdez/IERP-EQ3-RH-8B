    private void btn_RealizarOperacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RealizarOperacionActionPerformed
        RH_Asistencia nAsistencia = new RH_Asistencia();
        if(!isNew){
            nAsistencia.setIdAsistencia(asistencia.getIdAsistencia());
        }
        nAsistencia.setFecha(Date.valueOf(tmp_Fecha.getDate()));
        nAsistencia.setHoraEntrada(Time.valueOf(tmp_HoraInicio.getTime()));
        nAsistencia.setHoraSalida(Time.valueOf(tmp_HoraFinal.getTime()));
   
        String dias[] = new String[7];

        if (rbLunes.isSelected()) {
            dias[0] = "LUNES";
        }
        if (rbMartes.isSelected()) {
            dias[1] = "MARTES";
        }
        if (rbMiercoles.isSelected()) {
            dias[2] = "MIÉRCOLES";
        }
        if (rbJueves.isSelected()) {
            dias[3] = "JUEVES";
        }
        if (rbViernes.isSelected()) {
            dias[4] = "VIERNES";
        }
        if (rbSabado.isSelected()) {
            dias[5] = "SÁBADO";
        }
        if (rbDomingo.isSelected()) {
            dias[6] = "DOMINGO";
        }
        ArrayList<String> days = new ArrayList<>();

        for (int i = 0; i < 7; i++) {
            if (dias[i] != null) {
                days.add(dias[i]);
            }
        }
        String SEPARADOR = "";
        StringBuilder cadena = new StringBuilder();
        for (String s : days) {
            cadena.append(SEPARADOR);
            cadena.append(s);
            SEPARADOR = ",";
        }
        String diasT = cadena.toString();
        nAsistencia.setDia(diasT);
        AsistenciaDAO dao = new AsistenciaDAO(this.conexion);
        try {
            if (isNew) {
                if (dao.insertarAsistencia(nAsistencia)) {
                    JOptionPane.showMessageDialog(null, "Asistencia añadido con exito");
                    AsistenciasFrame asistenciasFrame = new AsistenciasFrame(this.conexion);
                    this.dispose();
                    asistenciasFrame.setVisible(true);
                    this.pack();
                } else {
                    JOptionPane.showMessageDialog(null, "Ocurrio un error al intentar crear la Asistencia");
                }
            } else {
                if (dao.actualizarAsistencia(nAsistencia)) {
                    JOptionPane.showMessageDialog(null, "Asistencia actualizada con exito");
                    AsistenciasFrame asistenciasFrame = new AsistenciasFrame(this.conexion);
                    this.dispose();
                    asistenciasFrame.setVisible(true);
                    this.pack();
                } else {
                    JOptionPane.showMessageDialog(null, "Ocurrio un error al intentar crear la asistencia");
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error ");
        }
    }//GEN-LAST:event_btn_RealizarOperacionActionPerformed

