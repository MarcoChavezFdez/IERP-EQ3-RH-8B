      public void verificarCampos() {
        if ("".equals(txtNombre.getText())) {
            btnRealizar.setEnabled(false);
            lblMensaje.setText("Debe llenar los campos");
        } else {
            btnRealizar.setEnabled(true);
            lblMensaje.setText("");
        }

    }
