    private void btnRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarActionPerformed
        RH_Departamento ndepa = new RH_Departamento();
        ndepa.setNombre(txtNombre.getText().toUpperCase());
        ndepa.setEstatus("A");
        DepartamentoDAO depaDAO = new DepartamentoDAO(this.conexion);
        try {
            if (bandera) {
                ndepa.setIdDepartamento(this.depa.getIdDepartamento());
                if (depaDAO.actualizarDepartamento(ndepa)) {
                    JOptionPane.showMessageDialog(null,  " Departamento Modificado con exito");
                    DepartamentoFrame depa = new DepartamentoFrame(this.conexion);
                    this.dispose();
                    depa.setVisible(true);
                    this.pack();
                }
            } else {
                if (depaDAO.insertarDepartamento(ndepa)) {
                    JOptionPane.showMessageDialog(null, "Departamento AÃ±adido con exito");
                    DepartamentoFrame depa = new DepartamentoFrame(this.conexion);
                    this.dispose();
                    depa.setVisible(true);
                    this.pack();
                }
            }

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error:" + e.getMessage());
        }

    }//GEN-LAST:event_btnRealizarActionPerformed

