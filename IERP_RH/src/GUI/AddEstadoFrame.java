    private void btn_RealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RealizarActionPerformed
        RH_Estado nEstado = new RH_Estado();
        nEstado.setNombre(txf_Nombre.getText().toUpperCase());
        nEstado.setSiglas(txf_Siglas.getText().toUpperCase());
        if (cmb_Estatus.getSelectedIndex() == 0) {
            nEstado.setEstatus("A");
        } else {
            nEstado.setEstatus("I");
        }
        EstadoDAO estadoDAO = new EstadoDAO(this.conexion);
        try {
            if (bandera) {
                if (estadoDAO.actualizarEstado(this.estado)) {
                    JOptionPane.showMessageDialog(null, "Estado A単adido con exito");
                    EstadosFrame estados = new EstadosFrame(this.conexion);
                    this.dispose();
                    estados.setVisible(true);
                    this.pack();
                }
            } else {
                if (estadoDAO.insertarEstado(nEstado)) {
                    JOptionPane.showMessageDialog(null, "Estado A単adido con exito");
                    EstadosFrame estados = new EstadosFrame(this.conexion);
                    this.dispose();
                    estados.setVisible(true);
                    this.pack();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error:" + e.getMessage());
        }

    }//GEN-LAST:event_btn_RealizarActionPerformed

