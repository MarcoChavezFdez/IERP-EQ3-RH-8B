    private void btn_RealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RealizarActionPerformed
        RH_FormaPago nFormaPago = new RH_FormaPago();
        nFormaPago.setNombre(txf_Nombre.getText().toUpperCase());
        nFormaPago.setEstatus("A");
        FormaPagoDAO daoFormaPago = new FormaPagoDAO(this.conexion);
        try {
            if (bandera) {
                nFormaPago.setIdFormaPago(this.formaPago.getIdFormaPago());
                if (daoFormaPago.actualizarFormaPago(nFormaPago)) {
                    JOptionPane.showMessageDialog(null, "Forma de Pago Modificada con exito");
                    FormasPagoFrame formaPago = new FormasPagoFrame(this.conexion);
                    this.dispose();
                    formaPago.setVisible(true);
                    this.pack();
                }
            } else {
                if (daoFormaPago.insertarFormaPago(nFormaPago)) {
                    JOptionPane.showMessageDialog(null, "Forma de Pago Añadida con exito");
                    FormasPagoFrame formaPago = new FormasPagoFrame(this.conexion);
                    this.dispose();
                    formaPago.setVisible(true);
                    this.pack();;
                }
            }

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error:" + e.getMessage());
        }

    }//GEN-LAST:event_btn_RealizarActionPerformed

