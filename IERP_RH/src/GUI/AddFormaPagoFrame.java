    public void verificarCampos() {
        if ("".equals(txf_Nombre.getText())) {
            btn_Realizar.setEnabled(false);
            lbl_Mensaje.setText("Debe llenar los campos");
        } else {
            btn_Realizar.setEnabled(true);
            lbl_Mensaje.setText("");
        }

    }
